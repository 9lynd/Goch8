package core

import (
	"fmt"
)

var fontset = [80]byte{
	0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
	0x20, 0x60, 0x20, 0x20, 0x70, // 1
	0xF0, 0x10, 0xF0, 0x80, 0xF0, // 2
	0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
	0x90, 0x90, 0xF0, 0x10, 0x10, // 4
	0xF0, 0x80, 0xF0, 0x10, 0xF0, // 5
	0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
	0xF0, 0x10, 0x20, 0x40, 0x40, // 7
	0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
	0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
	0xF0, 0x90, 0xF0, 0x90, 0x90, // A
	0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
	0xF0, 0x80, 0x80, 0x80, 0xF0, // C
	0xE0, 0x90, 0x90, 0x90, 0xE0, // D
	0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
	0xF0, 0x80, 0xF0, 0x80, 0x80, // F
}

const fontsetStart = 0x050

type CPU struct {
	v      [16]byte
	I      uint8
	PC     uint16
	SP     uint8
	Stack  [16]uint16
	Memory [4096]byte

	Display  *Display
	Keyboard *Keyboard

	DelayTimer byte
	SoundTimer byte
}

func NewCPU(display *Display, keyboard *Keyboard) *CPU {
	cpu := &CPU{
		PC:       0x200,
		Display:  display,
		Keyboard: keyboard,
	}

	for i, b := range fontset {
		cpu.Memory[fontsetStart+i] = b
	}

	return cpu
}

func (c *CPU) LoadROM(data []byte) error {
	if len(data) > len(c.Memory)-0x200 {
		return fmt.Errorf("ROM too large: %d bytes (max %d)", len(data), len(c.Memory)-0x200)
	}
	copy(c.Memory[0x200:], data)
	return nil
}

func (c *CPU) TickTimers() {
	if c.DelayTimer > 0 {
		c.DelayTimer--
	}
	if c.SoundTimer > 0 {
		c.SoundTimer--
	}
}
